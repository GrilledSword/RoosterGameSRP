using UnityEngine;

public class NetworkManagerFIX : MonoBehaviour
{
    public static NetworkManagerFIX Instance { get; private set; }

    void Awake()
    {
        if (Instance != null && Instance != this)
        {
            Destroy(gameObject);
            return;
        }
        Instance = this;
        DontDestroyOnLoad(gameObject);
    }
}
